my_foods = ['pizza','falafel','carrot cake','ice cream','corn dogs']

print(my_foods[0:3])
print(my_foods[:3])

print("\n")

print("Three items from the middle of my list are:")
print(my_foods[1:-1])

print("\n")

print("The last three times on my list are:")
print(my_foods[-3:])

